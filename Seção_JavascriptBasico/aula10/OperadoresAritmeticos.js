/*  
* Operadores Aritméticos
* + Adição / Concatenação
* - Subtração
* / Divisão
* * Multiplicação 
* ** Potenciação
* % Resto da divisão
*
* Ordem de Precedência em Javascript:
* () - Parênteses
* * | / | % - Multiplicação, Divisão e Resto
* - | + - Subtração e Adição
*/

// Adição

const num1 = 5;
const num2 = 10;
console.log(num1 + num2);

// Concatenação

const num3 = '5';
const num4 = 10;
console.log(num3 + num4);

// Subtração

console.log(num1 - num2);

// Divisão

console.log(num2 / num1);

// Multiplicação

console.log(num1 * num2);

// Potenciação

console.log(num1 ** num2);

// Resto % 

const num5 = 8;

console.log(num2 % num5)