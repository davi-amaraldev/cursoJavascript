// Operadores de Atribuição
/*
* += | Mais igual
* -= | Menos igual
* *= | Vezes Igual
* /= | Divisão Igual
*/

let contador = 1;
contador += 2; // 3
console.log(contador);

contador = 1; 
contador -= 2; // -1
console.log(contador);

contador = 1;
contador *= 2; // 2
console.log(contador);

contador = 1;
contador /= 2; // 0.5
console.log(contador);